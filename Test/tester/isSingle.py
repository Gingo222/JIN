# 判断字符串中{} [] ''是否成对出现


def is_str_single(parameter) -> bool:
    type_one, type_two, type_three = 1, 1, 0
    parameter_list = ''.join(parameter)
    for x in parameter_list:
        if x == '{':
            type_one = type_one + 1
        elif x == '}':
            type_one = type_one - 1
        elif x == '[':
            type_two = type_two + 1
        elif x == ']':
            type_two = type_two - 1
        elif x == "'":
            type_three = type_three + 1
        else:
            continue
    if type_one == 1 and type_two == 1 and type_three % 2 == 0:
        return True
    return False


